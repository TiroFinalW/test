package com.jielan.model.query;

import java.util.Date;

/**
 * Created by wang on 2016/12/20.
 */
public class SignRecordQuery extends PageQuery{

    private int id;
    private String openid;//用户openid
    private Integer type; //0:签到  1:补签
    private double peach;//签到获得的桃子;
    private Date signtime;//签到时间

    private String phone;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getOpenid() {
        return openid;
    }

    public void setOpenid(String openid) {
        this.openid = openid;
    }

    public double getPeach() {
        return peach;
    }

    public void setPeach(double peach) {
        this.peach = peach;
    }

    public Date getSigntime() {
        return signtime;
    }

    public void setSigntime(Date signtime) {
        this.signtime = signtime;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
